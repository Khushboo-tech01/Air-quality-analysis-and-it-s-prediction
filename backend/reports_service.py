"""PDF & CSV report generation."""
import io
from datetime import datetime, timezone
from typing import Dict, List

from reportlab.lib.pagesizes import A4
from reportlab.lib import colors
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import mm
from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle


def _styles():
    styles = getSampleStyleSheet()
    styles.add(ParagraphStyle(name="H", fontSize=22, leading=26, textColor=colors.HexColor("#111111"),
                              spaceAfter=12, fontName="Helvetica-Bold"))
    styles.add(ParagraphStyle(name="Sub", fontSize=11, textColor=colors.HexColor("#4b5563"),
                              spaceAfter=18))
    styles.add(ParagraphStyle(name="Section", fontSize=14, leading=18, textColor=colors.HexColor("#2563EB"),
                              spaceBefore=14, spaceAfter=8, fontName="Helvetica-Bold"))
    styles.add(ParagraphStyle(name="Body", fontSize=10, leading=14, textColor=colors.HexColor("#1f2937")))
    return styles


def build_prediction_pdf(prediction: Dict, user_email: str) -> bytes:
    buf = io.BytesIO()
    doc = SimpleDocTemplate(buf, pagesize=A4, topMargin=18 * mm, bottomMargin=18 * mm,
                            leftMargin=18 * mm, rightMargin=18 * mm)
    S = _styles()
    story = [
        Paragraph("AQI Prediction Report", S["H"]),
        Paragraph(f"Generated for {user_email} on {datetime.now(timezone.utc).strftime('%b %d, %Y %H:%M UTC')}", S["Sub"]),
        Paragraph("Prediction Result", S["Section"]),
    ]
    result_rows = [
        ["AQI Value", str(prediction.get("aqi", "—"))],
        ["Category", prediction.get("category", "—")],
        ["Health Advice", prediction.get("advice", "—")],
        ["Model Used", prediction.get("model", "—")],
        ["Location", prediction.get("location", "—")],
        ["Date", prediction.get("date", "—")],
    ]
    t = Table(result_rows, colWidths=[55 * mm, 115 * mm])
    t.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (0, -1), colors.HexColor("#F1F5F9")),
        ("TEXTCOLOR", (0, 0), (0, -1), colors.HexColor("#0f172a")),
        ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
        ("FONTSIZE", (0, 0), (-1, -1), 10),
        ("BOX", (0, 0), (-1, -1), 0.5, colors.HexColor("#e5e7eb")),
        ("INNERGRID", (0, 0), (-1, -1), 0.25, colors.HexColor("#e5e7eb")),
        ("VALIGN", (0, 0), (-1, -1), "TOP"),
        ("LEFTPADDING", (0, 0), (-1, -1), 8),
        ("RIGHTPADDING", (0, 0), (-1, -1), 8),
        ("TOPPADDING", (0, 0), (-1, -1), 8),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
    ]))
    story.append(t)
    story.append(Spacer(1, 12))
    story.append(Paragraph("Input Features", S["Section"]))
    feat_rows = [[k.upper(), str(v)] for k, v in (prediction.get("inputs") or {}).items()]
    if feat_rows:
        ft = Table(feat_rows, colWidths=[55 * mm, 115 * mm])
        ft.setStyle(TableStyle([
            ("FONTSIZE", (0, 0), (-1, -1), 10),
            ("BOX", (0, 0), (-1, -1), 0.5, colors.HexColor("#e5e7eb")),
            ("INNERGRID", (0, 0), (-1, -1), 0.25, colors.HexColor("#e5e7eb")),
            ("TEXTCOLOR", (0, 0), (0, -1), colors.HexColor("#475569")),
            ("LEFTPADDING", (0, 0), (-1, -1), 8),
            ("RIGHTPADDING", (0, 0), (-1, -1), 8),
            ("TOPPADDING", (0, 0), (-1, -1), 6),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
        ]))
        story.append(ft)
    story.append(Spacer(1, 20))
    story.append(Paragraph("This report was generated by AeroPulse — AQI Analysis & Prediction Platform.", S["Body"]))
    doc.build(story)
    buf.seek(0)
    return buf.read()


def build_model_metrics_pdf(dataset_name: str, results: List[Dict], best_model: str) -> bytes:
    buf = io.BytesIO()
    doc = SimpleDocTemplate(buf, pagesize=A4, topMargin=18 * mm, bottomMargin=18 * mm,
                            leftMargin=18 * mm, rightMargin=18 * mm)
    S = _styles()
    story = [
        Paragraph("Model Performance Report", S["H"]),
        Paragraph(f"Dataset: {dataset_name}", S["Sub"]),
        Paragraph(f"Best Model: {best_model}", S["Section"]),
    ]
    header = ["Model", "RMSE", "MAE", "R²", "CV R²", "Train (ms)", "Predict (ms)"]
    body_rows = [header] + [[r["name"], r["rmse"], r["mae"], r["r2"], r["cv_r2"], r["train_ms"], r["predict_ms"]] for r in results]
    t = Table(body_rows, colWidths=[45 * mm, 20 * mm, 20 * mm, 20 * mm, 20 * mm, 22 * mm, 25 * mm])
    t.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#2563EB")),
        ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
        ("FONTSIZE", (0, 0), (-1, -1), 9),
        ("ALIGN", (1, 1), (-1, -1), "RIGHT"),
        ("BOX", (0, 0), (-1, -1), 0.5, colors.HexColor("#e5e7eb")),
        ("INNERGRID", (0, 0), (-1, -1), 0.25, colors.HexColor("#e5e7eb")),
        ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, colors.HexColor("#F8FAFC")]),
        ("LEFTPADDING", (0, 0), (-1, -1), 6),
        ("RIGHTPADDING", (0, 0), (-1, -1), 6),
        ("TOPPADDING", (0, 0), (-1, -1), 6),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
    ]))
    story.append(t)
    doc.build(story)
    buf.seek(0)
    return buf.read()
